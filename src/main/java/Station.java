public class Station {

    private String StationName;
    private String ID;

    public void setName(String name) {
        this.StationName = name;
    }

    public void setID(String ID) {
        this.ID = ID;
    }

    public String getStationName() {
        return StationName;
    }

    public String getID() {
        return ID;
    }
}
